package io.nuls.account.rpc.model.form;

import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;

/**
 * @author: EdwardChan
 *
 * @since Mar. 22th 2019
 */
@ApiModel(value = "验证消息签名表单数据")
public class VerifyMessageSignatureForm {

    @ApiModelProperty(name = "address", value = "签名地址")
    private String address;

    @ApiModelProperty(name = "message", value = "被签名字符串")
    private String message;

    @ApiModelProperty(name = "signatureBase64", value = "签名字符串")
    private String signatureBase64;

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getSignatureBase64() {
        return signatureBase64;
    }

    public void setSignatureBase64(String signatureBase64) {
        this.signatureBase64 = signatureBase64;
    }
}
